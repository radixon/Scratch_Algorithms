	int *mergesort(int *a, int const len)
	{
		int q(0);
		double qr(0);
		qr = len/2;
		q = (int) qr;
		
		int n1 = q - 1;
		int n2 = len - q;
				
		const int s1 = n1 + 1;
		const int s2 = n2;
		
		
		int L[s1+1] = {0};
		int R[s2+1] = {0}; 
		
		int i(0);
		int j(0);
		int key(0);
			
		for(int j=0; j<=n1; j++)
		{
			L[j] = a[j];
			key = L[j];
			i = j - 1;
			while(i>=0 && L[i]>key)
			{
				L[i+1] = L[i];
				i = i - 1;
			}
			L[i+1] = key;
		}	
		
		
		for(int j=0; j<n2; j++)
		{
			R[j] = a[q+j];
			key = R[j];
			i = j - 1;
			while(i>=0 && R[i]>key)
			{
				R[i+1] = R[i];
				i = i - 1;
			}
			R[i+1] = key;
		}
		
				
		i = 0;
		j = 0;				
		for(int k=0; k<len; k++)
		{
			
			if (L[i] <= R[j]  && i <= n1)
			{				
				a[k] = L[i];
				i++;			
			}
			else
			{
				a[k] = R[j];
				j++;
			}			
		}
		
		return a;
	}
	
	
